export type LanguageType = 'en' | 'ar';

export function getLocalizedRoleDetails(roleType: RoleType, lang: 'en' | 'ar') {
  const t = translations[lang];
  switch (roleType) {
    case RoleType.CITIZEN:
      return {
        name: t.roleCitizenName,
        description: t.roleCitizenDesc,
        objective: t.roleCitizenObj,
        color: '#3B82F6',
        icon: 'Users'
      };
    case RoleType.MAFIA:
      return {
        name: t.roleMafiaName,
        description: t.roleMafiaDesc,
        objective: t.roleMafiaObj,
        color: '#EF4444',
        icon: 'Skull'
      };
    case RoleType.DOCTOR:
      return {
        name: t.roleDoctorName,
        description: t.roleDoctorDesc,
        objective: t.roleDoctorObj,
        color: '#10B981',
        icon: 'Shield'
      };
    case RoleType.DETECTIVE:
      return {
        name: t.roleDetectiveName,
        description: t.roleDetectiveDesc,
        objective: t.roleDetectiveObj,
        color: '#F59E0B',
        icon: 'Search'
      };
  }
}

